let bulkCreateSchedule = (inputData) => {
  return new Promise(async (resolve, reject) => {
    try {
      if (
        !inputData.arrSchedule ||
        !inputData.doctorId ||
        !inputData.formatedDate
      ) {
        resolve({
          errCode: 1,
          message: "Missing parameter.",
        });
      } else {
        let schedule = inputData.arrSchedule;
        if (schedule && schedule.length > 0) {
          schedule = schedule.map((item) => {
            item.maxNumber = MAX_NUMBER_SCHEDULE;
            return item;
          });
        }
        //get existing
        let existing = await db.Schedule.findAll({
          where: { doctorId: inputData.doctorId, date: inputData.formatedDate },
          attributes: ["timeType", "date", "doctorId", "maxNumber"],
          raw: true,
        });

        console.log("existing", existing);
        console.log("schedule", schedule);

        //compare different
        let toCreate = _.differenceWith(schedule, existing, (a, b) => {
          return +a.date === +b.date && a.timeType === b.timeType;
        });
        //create data
        if (toCreate && toCreate.length > 0) {
          await db.Schedule.bulkCreate(toCreate);
        }
        resolve({
          errCode: 0,
          message: "Save schedule Success",
        });
      }
    } catch (error) {
      reject(error);
    }
  });
};

